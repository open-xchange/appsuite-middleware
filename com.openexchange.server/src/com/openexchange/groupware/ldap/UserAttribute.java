package com.openexchange.groupware.ldap;

/**
 * Represents a user attribute with possible multiple values and their unique identifiers in form of a UUID.
 *
 * @author <a href="mailto:thorben.betten@open-xchange.com">Thorben Betten</a>
 */
public final class UserAttribute {

    private final String name;
    private final String value;
    private final int hash;

    public UserAttribute(String name, String value) {
        super();
        this.name = name;
        this.value = value;

        int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        hash = result;
    }

    /**
     * Gets the attribute's name
     *
     * @return The name
     */
    public String getName() {
        return name;
    }

    /**
     * Gets the attribute's value
     *
     * @return The value
     */
    public String getValue() {
        return value;
    }

    @Override
    public int hashCode() {
        return hash;
    }

    @Override
    public boolean equals(final Object obj) {
        if (this == obj) {
            return true;
        }
        if (!(obj instanceof UserAttribute)) {
            return false;
        }
        final UserAttribute other = (UserAttribute) obj;
        if (name == null) {
            if (other.name != null) {
                return false;
            }
        } else if (!name.equals(other.name)) {
            return false;
        }
        if (value == null) {
            if (other.value != null) {
                return false;
            }
        } else if (!value.equals(other.value)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        final StringBuilder builder = new StringBuilder(96);
        builder.append(name).append("=").append(value);
        return builder.toString();
    }
}
